// ========== CSP绕过支持 ==========
// 处理来自content script的CSP绕过请求

/**
 * 策略2：Background Fetch
 * 在background中fetch资源，通过消息传递给content script
 */
async function handleCSPBypassFetch(message, sender, sendResponse) {
  const { url, resourceType } = message

  try {
    console.log(`[CSPBypass] Background fetching: ${url}`)

    const response = await fetch(url, {
      method: 'GET',
      headers: {
        'Accept': resourceType === 'css' ? 'text/css' : 'application/javascript'
      }
    })

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`)
    }

    const code = await response.text()

    console.log(`[CSPBypass] Background fetch succeeded: ${code.length} bytes`)

    sendResponse({
      success: true,
      code,
      size: code.length
    })
  } catch (error) {
    console.error(`[CSPBypass] Background fetch failed:`, error)
    sendResponse({
      success: false,
      error: error.message
    })
  }

  return true // 保持消息通道开启
}

/**
 * 策略3：chrome.scripting.executeScript
 * 使用官方API注入脚本到页面
 */
async function handleCSPBypassScripting(message, sender, sendResponse) {
  const { url, code, resourceType } = message
  const tabId = sender.tab?.id

  if (!tabId) {
    sendResponse({
      success: false,
      error: 'No tab ID'
    })
    return true
  }

  try {
    console.log(`[CSPBypass] Scripting injecting to tab ${tabId}`)

    if (resourceType === 'js') {
      // 注入JS代码到MAIN世界
      await chrome.scripting.executeScript({
        target: { tabId },
        world: 'MAIN',
        func: (scriptCode) => {
          const script = document.createElement('script')
          script.textContent = scriptCode
          ;(document.head || document.documentElement).appendChild(script)
        },
        args: [code],
        injectImmediately: true
      })
    } else if (resourceType === 'css') {
      // 注入CSS
      await chrome.scripting.insertCSS({
        target: { tabId },
        css: code
      })
    }

    console.log(`[CSPBypass] Scripting injection succeeded`)

    sendResponse({
      success: true,
      size: code.length
    })
  } catch (error) {
    console.error(`[CSPBypass] Scripting injection failed:`, error)
    sendResponse({
      success: false,
      error: error.message
    })
  }

  return true // 保持消息通道开启
}

// 注册消息监听器
chrome.runtime.onMessage.addListener((message, sender, sendResponse) => {
  // CSP绕过 - Background Fetch
  if (message.type === 'CSP_BYPASS_FETCH') {
    handleCSPBypassFetch(message, sender, sendResponse)
    return true
  }

  // CSP绕过 - Scripting注入
  if (message.type === 'CSP_BYPASS_SCRIPTING') {
    handleCSPBypassScripting(message, sender, sendResponse)
    return true
  }
})

console.log('[Background] CSP bypass handlers registered')

// ========== JS资源DNR重定向规则 (ID 4000-4999) ==========
// 使用declarativeNetRequest在网络层拦截JS请求
// 规则由脚本自动生成：scripts/generate-dnr-rules.js

/**
 * 加载自动生成的DNR规则
 */
async function loadAutoGeneratedRules() {
  // 规则已在 background.js 中通过 importScripts 加载
  // 这里只检查是否已定义
  if (!self.AUTO_GENERATED_JS_REDIRECT_RULES) {
    console.warn('[Background] 自动生成规则未定义，跳过注册')
    return
  }
  console.log(`[Background] 已加载 ${self.AUTO_GENERATED_JS_REDIRECT_RULES.length} 条自动生成的DNR规则`)
}

/**
 * 注册JS重定向规则
 */
let _rulesRegistering = false

async function registerJSRedirectRules() {
  // 防止并发注册
  if (_rulesRegistering) {
    console.log('[Background] 规则正在注册中，跳过重复调用')
    return
  }

  // 检查是否禁用 CDN 重定向
  const settings = await chrome.storage.local.get('disableCDNRedirect')
  if (settings.disableCDNRedirect) {
    console.log('[Background] CDN 重定向已禁用，跳过规则注册')

    // 移除现有规则
    const existingRules = await chrome.declarativeNetRequest.getDynamicRules()
    const oldIds = existingRules
      .filter(rule => rule.id >= 4000 && rule.id < 5000)
      .map(rule => rule.id)

    if (oldIds.length > 0) {
      await chrome.declarativeNetRequest.updateDynamicRules({ removeRuleIds: oldIds })
      console.log('[Background] 已移除 CDN 重定向规则')
    }
    return
  }

  _rulesRegistering = true
  try {
    // 加载规则
    await loadAutoGeneratedRules()

    // 获取现有规则
    const existingRules = await chrome.declarativeNetRequest.getDynamicRules()
    const oldIds = existingRules
      .filter(rule => rule.id >= 4000 && rule.id < 5000)
      .map(rule => rule.id)

    // 移除旧规则
    if (oldIds.length > 0) {
      await chrome.declarativeNetRequest.updateDynamicRules({ removeRuleIds: oldIds })
    }

    // 注册新规则
    const rules = self.AUTO_GENERATED_JS_REDIRECT_RULES || []
    if (rules.length > 0) {
      await chrome.declarativeNetRequest.updateDynamicRules({
        addRules: rules
      })
      console.log(`[Background] JS重定向规则已注册: ${rules.length} 条`)
    }
  } catch (error) {
    console.error('[Background] JS重定向规则注册失败:', error)
  } finally {
    _rulesRegistering = false
  }
}

// 在扩展启动时注册规则
chrome.runtime.onStartup.addListener(() => {
  registerJSRedirectRules()
})

// 在安装时也注册
chrome.runtime.onInstalled.addListener(() => {
  registerJSRedirectRules()
})
